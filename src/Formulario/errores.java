    private void existePalabra(String text, String buscar) throws MiException{
        for (int i = 0; i <= text.length()-buscar.length(); i++) 
        {
         if(text.substring(i, i+buscar.length()).equals(buscar))
         {
             throw new MiException("Hay un error en la cadena");
         }
        }
        
    }

