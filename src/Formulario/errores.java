    private void textoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textoKeyTyped

        
         //capturar letra que esta presionando evt.getKeyChar()
        if(!Character.isLetter(evt.getKeyChar())&& !(evt.getKeyChar()== KeyEvent.VK_SPACE)
                && !(evt.getKeyChar() == KeyEvent.VK_BACK_SPACE)
                && !(evt.getKeyChar() == KeyEvent.VK_DELETE))
        {
            evt.consume();
            lblError.setText("Escribe solo letras");
        }
        else
            lblError.setText("");
    }//GEN-LAST:event_textoKeyTyped

    private void textoNumerosKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textoNumerosKeyTyped
     if(!Character.isDigit(evt.getKeyChar())&& !(evt.getKeyChar()== KeyEvent.VK_SPACE)
                && !(evt.getKeyChar() == KeyEvent.VK_BACK_SPACE)
                && !(evt.getKeyChar() == KeyEvent.VK_DELETE)
             )
        {
            evt.consume();
            lblErrorNumeros.setText("Escribe solo letras");
        }
        else
            lblErrorNumeros.setText("");
    }//GEN-LAST:event_textoNumerosKeyTyped

